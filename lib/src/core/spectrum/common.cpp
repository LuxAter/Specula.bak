bool specula::spectrum_samples_sorted(const Float *lambda,
                                      const Float * /*unused*/, std::size_t n) {
  for (std::size_t i = 0; i < n - 1; ++i) {
    if (lambda[i] > lambda[i + 1]) {
      return false;
    }
  }
  return true;
}

void specula::sort_spectrum_samples(Float *lambda, Float *vals, std::size_t n) {
  std::vector<std::pair<Float, Float>> sort_vec;
  sort_vec.reserve(n);
  for (std::size_t i = 0; i < n; ++i) {
    sort_vec.emplace_back(lambda[i], vals[i]);
  }
  std::sort(sort_vec.begin(), sort_vec.end());
  for (std::size_t i = 0; i < n; ++i) {
    lambda[i] = sort_vec[i].first;
    vals[i] = sort_vec[i].second;
  }
}

specula::Float specula::average_spectrum_samples(const Float *lambda,
                                                 const Float *vals,
                                                 std::size_t n,
                                                 Float lambda_start,
                                                 Float lambda_end) {
  for (std::size_t i = 0; i < n - 1; ++i) {
    CHECK_GT(lambda[i + 1], lambda[i]);
  }
  CHECK_LT(lambda_start, lambda_end);
  if (lambda_end <= lambda[0]) {
    return vals[0];
  }
  if (lambda_start >= lambda[n - 1]) {
    return vals[n - 1];
  }
  if (n == 1) {
    return vals[0];
  }
  Float sum = 0;
  if (lambda_start < lambda[0]) {
    sum += vals[0] * (lambda[0] - lambda_start);
  }
  if (lambda_end > lambda[n - 1]) {
    sum += vals[n - 1] * (lambda_end - lambda[n - 1]);
  }

  std::size_t i = 0;
  while (lambda_start > lambda[i + 1]) {
    ++i;
  }
  CHECK_LT(i + 1, n);
  auto interp = [lambda, vals](Float w, std::size_t i) {
    return lerp((w - lambda[i]) / (lambda[i + 1] - lambda[i]), vals[i],
                vals[i + 1]);
  };
  for (; i + 1 < n && lambda_end >= lambda[i]; ++i) {
    Float seg_lambda_start = std::max(lambda_start, lambda[i]);
    Float seg_lambda_end = std::min(lambda_end, lambda[i + 1]);
    sum += 0.5 * (interp(seg_lambda_start, i) + interp(seg_lambda_end, i)) *
           (seg_lambda_end - seg_lambda_start);
  }
  return sum / (lambda_end - lambda_start);
}
specula::Float specula::interpolate_spectrum_samples(const Float *lambda,
                                                     const Float *vals,
                                                     std::size_t n, Float l) {
  for (std::size_t i = 0; i < n - 1; ++i) {
    CHECK_GT(lambda[i + 1], lambda[i]);
  }
  if (l <= lambda[0]) {
    return vals[0];
  }
  if (l >= lambda[n - 1]) {
    return vals[n - 1];
  }
  std::size_t offset =
      find_interval(n, [&](std::size_t index) { return lambda[index] <= l; });
  CHECK(l >= lambda[offset] && l <= lambda[offset + 1]);
  Float t = (l - lambda[offset]) / (lambda[offset + 1] - lambda[offset]);
  return lerp(t, vals[offset], vals[offset + 1]);
}

void specula::blackbody(const Float *lambda, std::size_t n, Float T,
                        Float *Le) {
  if (T <= 0) {
    for (std::size_t i = 0; i < n; ++i) {
      Le[i] = 0.0F;
    }
    return;
  }
  const Float c = 299792458;
  const Float h = 6.62606957e-34;
  const Float kb = 1.3806488e-23;
  for (std::size_t i = 0; i < n; ++i) {
    Float l = lambda[i] * 1e-9;
    Float lambda5 = (l * l) * (l * l) * l;
    Le[i] =
        (2 * h * c * c) / (lambda5 * (std::exp((h * c) / (l * kb * T)) - 1));
    CHECK(!std::isnan(Le[i]));
  }
}
void specula::blackbody_normalized(const Float *lambda, std::size_t n, Float T,
                                   Float *Le) {
  blackbody(lambda, n, T, Le);
  Float lambda_max = 2.8977721e-3 / T * 1e9;
  Float max_l;
  blackbody(&lambda_max, 1, T, &max_l);
  for (std::size_t i = 0; i < n; ++i) {
    Le[i] /= max_l;
  }
}

void specula::resample_linear_spectrum(const Float *lambda_in,
                                       const Float *v_in, std::size_t n_in,
                                       Float lambda_min, Float lambda_max,
                                       std::size_t n_out, Float *v_out) {
  CHECK_GE(n_out, 2);
  for (std::size_t i = 0; i < n_in - 1; ++i) {
    CHECK_GT(lambda_in[i + 1], lambda_in[i]);
  }
  CHECK_LT(lambda_min, lambda_max);
  Float delta = (lambda_max - lambda_min) / (n_out - 1);

  auto lambda_in_clamped = [&](int index) {
    CHECK(index >= -1 && index <= static_cast<int>(n_in));
    if (index == -1) {
      CHECK_LT(lambda_min - delta, lambda_in[0]);
      return lambda_min - delta;
    }
    if (index == static_cast<int>(n_in)) {
      CHECK_GT(lambda_max + delta, lambda_in[n_in - 1]);
      return lambda_max + delta;
    }
    return lambda_in[index];
  };
  auto v_in_clamped = [&](int index) {
    CHECK(index >= -1 && index <= static_cast<int>(n_in));
    return v_in[clamp(index, 0, static_cast<int>(n_in) - 1)];
  };
  auto resample = [&](Float lambda) -> Float {
    if (lambda + delta / 2 <= lambda_in[0]) {
      return v_in[0];
    }
    if (lambda - delta / 2 >= lambda_in[n_in - 1]) {
      return v_in[n_in - 1];
    }
    if (n_in == 1) {
      return v_in[0];
    }
    int start;
    int end;
    if (lambda - delta < lambda_in[0]) {
      start = -1;
    } else {
      start = find_interval(
          n_in, [&](int i) { return lambda_in[i] <= lambda - delta; });
      CHECK(start >= 0 && start < static_cast<int>(n_in));
    }
    if (lambda + delta > lambda_in[n_in - 1]) {
      end = n_in;
    } else {
      end = start > 0 ? start : 0;
      while (end < static_cast<int>(n_in) && lambda + delta > lambda_in[end]) {
        ++end;
      }
    }
    if (end - start == 2 && lambda_in_clamped(start) <= lambda - delta &&
        lambda_in[start + 1] == lambda &&
        lambda_in_clamped(end) >= lambda + delta) {
      return v_in[start + 1];
    }
    if (end - start == 1) {
      Float t = (lambda - lambda_in_clamped(start)) /
                (lambda_in_clamped(end) - lambda_in_clamped(start));
      CHECK(t >= 0 && t <= 1);
      return lerp(t, v_in_clamped(start), v_in_clamped(end));
    }
    return average_spectrum_samples(lambda_in, v_in, n_in, lambda - delta / 2,
                                    lambda + delta / 2);
  };

  for (std::size_t out_offset = 0; out_offset < n_out; ++out_offset) {
    Float lambda =
        lerp(Float(out_offset) / (n_out - 1), lambda_min, lambda_max);
    v_out[out_offset] = resample(lambda);
  }
}
